import os
import shutil
import socket
import subprocess
import sys
from collections.abc import Callable, Mapping, Sequence
from pathlib import Path
from typing import Final

from volte_mutation_fuzzer.softphone import SoftphoneConfigError, resolve_baresip_binary

BARESIP_DEFAULT_CONFIG_DIR: Final[Path] = Path.home() / ".baresip"
BARESIP_DEFAULT_SIP_PORT: Final[int] = 5060
BARESIP_DEFAULT_ACCOUNT_URI: Final[str] = "<sip:fuzztarget@127.0.0.1>"

SOFTPHONE_CONFIG_DIR_ENV: Final[str] = "VMF_SOFTPHONE_CONFIG_DIR"

_SUPPORTED_PLATFORMS: Final[dict[str, str]] = {
    "darwin": "brew",
    "linux": "apt-get",
}


class SoftphoneSetupError(RuntimeError):
    """Raised when softphone installation or provisioning fails."""


def detect_platform() -> str:
    platform = sys.platform
    if platform.startswith("linux"):
        return "linux"
    if platform == "darwin":
        return "darwin"
    raise SoftphoneSetupError(
        f"unsupported platform: {platform}. Only darwin and linux are supported."
    )


def find_package_manager(platform: str) -> str:
    package_manager = _SUPPORTED_PLATFORMS.get(platform)
    if package_manager is None:
        raise SoftphoneSetupError(
            f"no package manager configured for platform: {platform}"
        )

    found = shutil.which(package_manager)
    if found is None:
        raise SoftphoneSetupError(
            f"package manager not found: {package_manager}. "
            "Install it first or set VMF_SOFTPHONE_BIN to a custom baresip binary."
        )
    return found


def is_baresip_installed(env: Mapping[str, str] | None = None) -> bool:
    try:
        resolve_baresip_binary(env)
        return True
    except SoftphoneConfigError:
        return False


def install_baresip(
    *,
    runner: Callable[
        ..., subprocess.CompletedProcess[str] | subprocess.CompletedProcess[bytes]
    ]
    | None = None,
) -> None:
    platform = detect_platform()
    package_manager = find_package_manager(platform)

    if platform == "darwin":
        command = [package_manager, "install", "baresip"]
    else:
        command = ["sudo", package_manager, "install", "-y", "baresip"]

    execute = runner or subprocess.run
    result = execute(command, check=False)
    if result.returncode != 0:
        raise SoftphoneSetupError(
            f"baresip installation failed with exit code {result.returncode}. "
            f"Command: {' '.join(command)}"
        )


def _find_baresip_module_path() -> str | None:
    """Locate the baresip modules directory from the installed binary."""
    binary = shutil.which("baresip")
    if binary is None:
        return None
    # Resolve symlinks: /opt/homebrew/bin/baresip → .../Cellar/baresip/x.y.z/bin/baresip
    resolved = Path(binary).resolve()
    # Walk up to find lib/baresip/modules relative to the prefix
    for parent in resolved.parents:
        candidate = parent / "lib" / "baresip" / "modules"
        if candidate.is_dir():
            return str(candidate)
    return None


def generate_baresip_config(sip_port: int = BARESIP_DEFAULT_SIP_PORT) -> str:
    module_path = _find_baresip_module_path()
    module_path_line = f"module_path\t\t{module_path}\n" if module_path else ""
    return f"""\
# Generated by volte-mutation-fuzzer softphone-setup
# Minimal config for headless SIP fuzzing target

sip_listen\t\t0.0.0.0:{sip_port}
{module_path_line}
# Disable audio I/O for headless operation
audio_player\t\tnone
audio_source\t\tnone
audio_alert\t\tnone

# Disable video
video_display\t\tnone
video_source\t\tnone

# Essential modules for SIP handling and process keepalive
module\t\t\tuuid.so
module\t\t\taccount.so
module\t\t\tcons.so
module\t\t\tmenu.so
module\t\t\tg711.so
"""


def generate_baresip_accounts(
    account_uri: str = BARESIP_DEFAULT_ACCOUNT_URI,
) -> str:
    return f"{account_uri}\n"


def provision_config_dir(
    config_dir: Path,
    *,
    sip_port: int = BARESIP_DEFAULT_SIP_PORT,
    account_uri: str = BARESIP_DEFAULT_ACCOUNT_URI,
) -> Path:
    config_dir.mkdir(parents=True, exist_ok=True)
    (config_dir / "config").write_text(
        generate_baresip_config(sip_port), encoding="utf-8"
    )
    (config_dir / "accounts").write_text(
        generate_baresip_accounts(account_uri), encoding="utf-8"
    )
    return config_dir


def _resolve_config_dir(config_dir: Path | None, env: Mapping[str, str]) -> Path:
    if config_dir is not None:
        return config_dir
    from_env = env.get(SOFTPHONE_CONFIG_DIR_ENV, "").strip()
    if from_env:
        return Path(from_env).expanduser()
    return BARESIP_DEFAULT_CONFIG_DIR


def setup(
    *,
    install: bool = True,
    config_dir: Path | None = None,
    sip_port: int = BARESIP_DEFAULT_SIP_PORT,
    runner: Callable[
        ..., subprocess.CompletedProcess[str] | subprocess.CompletedProcess[bytes]
    ]
    | None = None,
) -> Path:
    env = os.environ

    if install and not is_baresip_installed():
        print(
            "[vmf softphone-setup] baresip not found — installing...", file=sys.stderr
        )
        install_baresip(runner=runner)
        print("[vmf softphone-setup] baresip installed.", file=sys.stderr)
    else:
        print("[vmf softphone-setup] baresip already installed.", file=sys.stderr)

    resolved_dir = _resolve_config_dir(config_dir, env)
    local_ip = _get_local_ip()
    account_uri = f"<sip:fuzztarget@{local_ip}>;regint=0"
    provision_config_dir(resolved_dir, sip_port=sip_port, account_uri=account_uri)
    print(
        f"[vmf softphone-setup] config provisioned at: {resolved_dir}", file=sys.stderr
    )

    return resolved_dir


def _get_local_ip() -> str:
    """Return the primary non-loopback IPv4 address of this machine."""
    with socket.socket(socket.AF_INET, socket.SOCK_DGRAM) as s:
        try:
            s.connect(("8.8.8.8", 80))
            return s.getsockname()[0]
        except OSError:
            return "127.0.0.1"


def main(argv: Sequence[str] | None = None) -> None:
    del argv

    try:
        config_dir = setup()
        local_ip = _get_local_ip()
        print(
            f"[vmf softphone-setup] target Baresip at: {local_ip}:{BARESIP_DEFAULT_SIP_PORT}",
            file=sys.stderr,
        )
        print(str(config_dir))
    except SoftphoneSetupError as exc:
        print(f"[vmf softphone-setup] {exc}", file=sys.stderr)
        raise SystemExit(2) from exc
    except OSError as exc:
        print(f"[vmf softphone-setup] failed: {exc}", file=sys.stderr)
        raise SystemExit(2) from exc
    except KeyboardInterrupt as exc:
        raise SystemExit(130) from exc


__all__ = [
    "BARESIP_DEFAULT_ACCOUNT_URI",
    "BARESIP_DEFAULT_CONFIG_DIR",
    "BARESIP_DEFAULT_SIP_PORT",
    "SOFTPHONE_CONFIG_DIR_ENV",
    "SoftphoneSetupError",
    "detect_platform",
    "find_package_manager",
    "generate_baresip_accounts",
    "generate_baresip_config",
    "install_baresip",
    "is_baresip_installed",
    "main",
    "provision_config_dir",
    "setup",
]


if __name__ == "__main__":
    main()
